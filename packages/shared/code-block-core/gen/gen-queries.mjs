#!/usr/bin/env node
// Generates src/queries.h by composing each language's tree-sitter highlights
// query from the vendored `.scm` files. Run after refreshing a grammar.
//
//   node gen/gen-queries.mjs
//
// Composition order matters: the flattener resolves overlapping captures with
// "innermost wins, later pattern wins ties", so more specific queries are
// concatenated AFTER the broad base (see vendor/VENDOR.md).

import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const g = (p) => resolve(root, "vendor/grammars", p);
// Local (non-vendored) overlay queries live outside vendor/ so the vendored
// trees stay byte-identical to upstream.
const l = (p) => resolve(root, "queries", p);

const F = {
    jsHighlights: g("javascript/queries/highlights.scm"),
    jsJsx: g("javascript/queries/highlights-jsx.scm"),
    jsParams: g("javascript/queries/highlights-params.scm"),
    jsEscapes: l("javascript-escapes.scm"),
    markdownLocal: l("markdown.scm"),
    tsHighlights: g("typescript/highlights.scm"),
    python: g("python/queries/highlights.scm"),
    kotlin: g("kotlin/queries/highlights.scm"),
    swift: g("swift/queries/highlights.scm"),
    bash: g("bash/queries/highlights.scm"),
    c: g("c/queries/highlights.scm"),
    css: g("css/queries/highlights.scm"),
    go: g("go/queries/highlights.scm"),
    html: g("html/queries/highlights.scm"),
    java: g("java/queries/highlights.scm"),
    json: g("json/queries/highlights.scm"),
    markdown: g("markdown/queries/highlights.scm"),
    markdownInline: g("markdown-inline/queries/highlights.scm"),
    ruby: g("ruby/queries/highlights.scm"),
    rust: g("rust/queries/highlights.scm")
};

// language name (already normalized) -> ordered list of scm files
// The js `highlights-params` query is structured for the javascript grammar
// (`formal_parameters` directly containing identifiers). TypeScript wraps
// parameters in required/optional_parameter nodes, so its own highlights.scm
// covers them and the js params query would be a structural mismatch there.
const COMPOSE = {
    javascript: [F.jsHighlights, F.jsParams, F.jsEscapes],
    jsx: [F.jsHighlights, F.jsJsx, F.jsParams, F.jsEscapes],
    typescript: [F.jsHighlights, F.tsHighlights, F.jsEscapes],
    tsx: [F.jsHighlights, F.jsJsx, F.tsHighlights, F.jsEscapes],
    python: [F.python],
    kotlin: [F.kotlin],
    swift: [F.swift],
    bash: [F.bash],
    c: [F.c],
    css: [F.css],
    go: [F.go],
    html: [F.html],
    java: [F.java],
    json: [F.json],
    markdown: [F.markdown, F.markdownLocal],
    ruby: [F.ruby],
    rust: [F.rust]
};

const INTERNAL_COMPOSE = {
    markdown_inline: [F.markdownInline]
};

function compose(files) {
    return files.map((file) => readFileSync(file, "utf8").trimEnd()).join("\n\n");
}

function toCLiteral(text) {
    // Emit one quoted chunk per source line for readable, diffable output.
    return text
        .split("\n")
        .map((line) => {
            const escaped = line
                .replace(/\\/g, "\\\\")
                .replace(/"/g, '\\"')
                .replace(/\t/g, "\\t")
                // Escape '?' so operator literals like "??=" can't form C trigraphs.
                .replace(/\?/g, "\\?");
            return `    "${escaped}\\n"`;
        })
        .join("\n");
}

function renderQueries() {
    const languages = Object.keys(COMPOSE);
    const querySources = { ...COMPOSE, ...INTERNAL_COMPOSE };
    let output = "";
    output += "// Generated by gen/gen-queries.mjs. Do not edit by hand.\n";
    output += "#ifndef CODE_BLOCK_QUERIES_H\n";
    output += "#define CODE_BLOCK_QUERIES_H\n\n";

    for (const [lang, files] of Object.entries(querySources)) {
        const source = compose(files);
        output += `static const char *cb_query_${lang} =\n${toCLiteral(source)};\n\n`;
    }

    output += "static const char *cb_query_source(const char *language) {\n";
    for (const lang of languages) {
        output += `    if (strcmp(language, "${lang}") == 0) return cb_query_${lang};\n`;
    }
    output += "    return 0;\n";
    output += "}\n\n";
    output += "#endif\n";
    return { languages, output };
}

const dest = resolve(root, "src/queries.h");
const { languages, output } = renderQueries();

if (process.argv.includes("--check")) {
    const current = existsSync(dest) ? readFileSync(dest, "utf8") : "";
    if (current !== output) {
        console.error("src/queries.h is out of date; run pnpm run build:queries");
        process.exitCode = 1;
    } else {
        console.log(`src/queries.h is current (${languages.length} languages)`);
    }
} else {
    writeFileSync(dest, output);
    console.log(`Wrote ${dest} (${languages.length} languages)`);
}
