import { createRequire } from "node:module";
import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = dirname(dirname(fileURLToPath(import.meta.url)));
const outputPath = join(repoRoot, "packages/prism-code-core/generated/prism-code.js");
const coreSourcePath = join(repoRoot, "packages/prism-code-core/src/prism-code-core.ts");
const nativeSourcePath = join(repoRoot, "packages/prism-code-core/src/native-prism-code.ts");
const requireFromReact = createRequire(join(repoRoot, "packages/react-code-block/package.json"));
const requireFromRoot = createRequire(join(repoRoot, "package.json"));
const ts = requireFromRoot("typescript");

const prismComponents = [
  "markup",
  "css",
  "clike",
  "javascript",
  "bash",
  "go",
  "java",
  "json",
  "jsx",
  "kotlin",
  "python",
  "ruby",
  "rust",
  "swift",
  "typescript",
  "tsx"
];

function compile(sourcePath) {
  const source = readFileSync(sourcePath, "utf8");
  return ts
    .transpileModule(source, {
      compilerOptions: {
        esModuleInterop: true,
        module: ts.ModuleKind.CommonJS,
        removeComments: false,
        strict: true,
        target: ts.ScriptTarget.ES5
      },
      fileName: sourcePath
    })
    .outputText.trimEnd();
}

function readPrismSources() {
  const sources = [
    readFileSync(requireFromReact.resolve("prismjs/components/prism-core.js"), "utf8")
  ];
  for (const component of prismComponents) {
    sources.push(
      readFileSync(requireFromReact.resolve(`prismjs/components/prism-${component}.js`), "utf8")
    );
  }
  return sources.join("\n\n");
}

function buildPrismCodeIIFE() {
  const prismSource = readPrismSources();
  const compiledCore = compile(coreSourcePath);
  const compiledNativeHost = compile(nativeSourcePath);

  return [
    "// Generated from packages/prism-code-core/src by scripts/build-prism-code.mjs.",
    "// Do not edit this file by hand.",
    "(function (global) {",
    "  global.globalThis = global.globalThis || global;",
    "  global.self = global.self || global;",
    "  global.window = global.window || global;",
    indent(prismSource),
    "  var coreExports = {};",
    "  var coreModule = { exports: coreExports };",
    "  var exports = coreExports;",
    "  var module = coreModule;",
    indent(compiledCore),
    "  coreExports = coreModule.exports;",
    "  var PrismCodeCore = coreExports;",
    "  var hostExports = {};",
    "  var hostModule = { exports: hostExports };",
    "  exports = hostExports;",
    "  module = hostModule;",
    indent(compiledNativeHost),
    "  hostExports = hostModule.exports;",
    '  if (typeof hostExports.installPrismCodeBridge !== "function") {',
    '    throw new Error("Generated Prism bridge is missing installPrismCodeBridge().");',
    "  }",
    "  hostExports.installPrismCodeBridge(global);",
    "}(this));",
    ""
  ].join("\n");
}

function indent(value) {
  return value
    .split("\n")
    .map((line) => (line.length > 0 ? `  ${line}` : line))
    .join("\n");
}

function writePrismCode() {
  const output = buildPrismCodeIIFE();
  mkdirSync(dirname(outputPath), { recursive: true });
  writeFileSync(outputPath, output);
  console.log(
    "generated packages/prism-code-core/generated/prism-code.js from prism-code-core TypeScript"
  );
}

function checkPrismCode() {
  const expected = buildPrismCodeIIFE();
  const actual = existsSync(outputPath) ? readFileSync(outputPath, "utf8") : "";
  if (actual !== expected) {
    console.error(
      "packages/prism-code-core/generated/prism-code.js is out of date; run pnpm run build:prism-code"
    );
    process.exitCode = 1;
    return;
  }
  console.log("prism-code.js is up to date");
}

if (process.argv.includes("--check")) {
  checkPrismCode();
} else {
  writePrismCode();
}
