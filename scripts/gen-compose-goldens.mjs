import {
    copyFileSync,
    existsSync,
    mkdirSync,
    readFileSync,
    readdirSync,
    rmSync,
    writeFileSync
} from "node:fs";
import { execFileSync } from "node:child_process";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), "..");
const fixturesPath = join(repoRoot, "packages/shared/code-block-core/fixtures/code-snippets.json");
const goldenSrc = join(repoRoot, "packages/shared/code-block-core/test/golden/token-stream");
const testRoot = join(repoRoot, "packages/compose-code-block/src/test");
const resourcesDir = join(testRoot, "resources/golden/token-stream");
const corpusFile = join(
    testRoot,
    "kotlin/io/github/dongyuzhao/composecodeblock/GoldenCodeCorpus.kt"
);

const snippets = JSON.parse(readFileSync(fixturesPath, "utf8"));

function kotlinStringLiteral(value) {
    return JSON.stringify(value).replaceAll("$", "\\$");
}

const entries = snippets
    .map(
        (snippet) =>
            `    GoldenCodeSnippetEntry(${kotlinStringLiteral(snippet.id)}, ${kotlinStringLiteral(
                snippet.language
            )}, ${kotlinStringLiteral(snippet.code)}, ${snippet.fontSize}f)`
    )
    .join(",\n");

const output = `package io.github.dongyuzhao.composecodeblock

// GENERATED by scripts/gen-compose-goldens.mjs from
// packages/shared/code-block-core/fixtures/code-snippets.json. Do not edit by hand.

internal data class GoldenCodeSnippetEntry(
    val id: String,
    val language: String,
    val code: String,
    val fontSizeSp: Float
)

internal val goldenCodeSnippets: List<GoldenCodeSnippetEntry> = listOf(
${entries}
)
`;

const goldenNames = () =>
    readdirSync(goldenSrc)
        .filter((name) => name.endsWith(".json"))
        .sort();

if (process.argv.includes("--check")) {
    const expectedGoldenNames = goldenNames();
    const resourceNames = existsSync(resourcesDir)
        ? readdirSync(resourcesDir)
              .filter((name) => name.endsWith(".json"))
              .sort()
        : [];
    const resourcesMatch =
        JSON.stringify(resourceNames) === JSON.stringify(expectedGoldenNames) &&
        expectedGoldenNames.every(
            (name) =>
                readFileSync(join(goldenSrc, name), "utf8") ===
                readFileSync(join(resourcesDir, name), "utf8")
        );
    const corpusMatches = existsSync(corpusFile) && readFileSync(corpusFile, "utf8") === output;

    if (!resourcesMatch || !corpusMatches) {
        console.error("Compose goldens are out of date; run pnpm run gen:compose-goldens");
        process.exitCode = 1;
    } else {
        console.log(`Compose goldens are current (${snippets.length} fixtures)`);
    }
} else {
    execFileSync("make", ["-C", join(repoRoot, "packages/shared/code-block-core"), "all"], {
        stdio: "inherit"
    });
    execFileSync(
        process.execPath,
        [join(repoRoot, "packages/shared/code-block-core/gen/gen-goldens.mjs")],
        { stdio: "inherit" }
    );

    const generatedGoldenNames = goldenNames();
    rmSync(resourcesDir, { recursive: true, force: true });
    mkdirSync(resourcesDir, { recursive: true });
    for (const name of generatedGoldenNames) {
        copyFileSync(join(goldenSrc, name), join(resourcesDir, name));
    }
    mkdirSync(dirname(corpusFile), { recursive: true });
    writeFileSync(corpusFile, output);
    console.log(`synced ${snippets.length} token-stream goldens -> src/test/resources/golden`);
    console.log(`generated GoldenCodeCorpus.kt with ${snippets.length} entries`);
}
